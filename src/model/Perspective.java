package model;

public class Perspective {
    private double scaleFactor;
    private double translationAxeY;
    private double translationAxeX;

    public double getScaleFactor() {
        return scaleFactor;
    }

    public void setScaleFactor(double scaleFactor) {
        this.scaleFactor = scaleFactor;
    }

    public double getTranslationAxeY() {
        return translationAxeY;
    }

    public void setTranslationAxeY(double translationAxeY) {
        this.translationAxeY = translationAxeY;
    }

    public double getTranslationAxeX() {
        return translationAxeX;
    }

    public void setTranslationAxeX(double translationAxeX) {
        this.translationAxeX = translationAxeX;
    }
}
